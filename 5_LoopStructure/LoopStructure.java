import java.util.*;

public class LoopStructure {
    public static void main(String[] args) {
        // 以下指的 C艹 标准是 C艹98

        // while, do while 循环与 C艹 一致

        // for 循环除了与 C艹 一致的用法外，还可以
        String [] names ={"ya", "yaya", "yayaya", "yayayaya"};
        for( String name : names ) {
            System.out.print( name );
            System.out.print(",");
        }

        // break, continue 与 C艹 一致
    }
}
